package com.jachs.commons.dbutils;

public class test {
	private String name;
	private int age;
	private String area;
	public test() {
		super();
	}
	public test(String name, int age, String area) {
		super();
		this.name = name;
		this.age = age;
		this.area = area;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	
}
